package egovframework.smebridge.admin.survey.model;

public class AdminSurveyQuestionVo {
	
	String idx = "";
	String survey_idx = "";
	String seq = "";
	String field_name = "";
	String type = "";
	String select_list = "";
	String coment = "";
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getSurvey_idx() {
		return survey_idx;
	}
	public void setSurvey_idx(String survey_idx) {
		this.survey_idx = survey_idx;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getField_name() {
		return field_name;
	}
	public void setField_name(String field_name) {
		this.field_name = field_name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSelect_list() {
		return select_list;
	}
	public void setSelect_list(String select_list) {
		this.select_list = select_list;
	}
	public String getComent() {
		return coment;
	}
	public void setComent(String coment) {
		this.coment = coment;
	}
	
	

}
